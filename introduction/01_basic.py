import numpy as np # Learning that numpy is usually imported as np

print(f"The numpy version is: {np.__version__}") # Printing out the version

# Creating a simple numpy object
example = [1, 2, 3, 4, 5, 6]
numpy_object = np.array(example)

# Printing out some information regarding the object
print(numpy_object)
print(type(numpy_object)) # object is <class 'numpy.ndarray'>
